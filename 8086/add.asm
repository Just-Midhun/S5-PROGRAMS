DATA SEGMENT
MSG1 	DB "ENTER THE FIRST NUMBER:$"
MSG2	DB "ENTER THE SECOND NUMBER:$"
MSG	DB "SUM OF THE TWO NUMBERS:$"
DATA ENDS
CODE SEGMENT

START:
MOV AX,DATA
MOV DS,AX

LEA DX,MSG1
MOV AH,09H
INT 21H
MOV AH,01H
INT 21H
MOV BL,AL

LEA DX,MSG2
MOV AH,09H
INT 21H
MOV AH,01H
INT 21H
ADD AL,BL
MOV AH,00H
AAA
ADD AX,3030H
LEA DX,MSG
MOV AH,09H
INT 21H
MOV DL,AL
MOV AH,02H
INT 21H
MOV AH,4CH
INT 21H
CODE ENDS
END START
